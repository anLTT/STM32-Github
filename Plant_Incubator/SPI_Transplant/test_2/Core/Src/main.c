void transfer_mono_data_8pixel(uint8_t dat1)
{
	transfer_mono_data_3pixel(dat1);//传送 dat1 的 D7\D6\D5 这 3 位，对应 3 个点阵(第 1、2、3 个）会显示出来；列地址是自动+1 的
	transfer_mono_data_3pixel(dat1<<3);//传送 dat1 的 D4\D3\D2 这 3 位，对应 3 个点阵(第 4、5、6 个）会显示出来；列地址是自动+1 的
	transfer_mono_data_3pixel(dat1<<6);//传送 dat1 的 D1\D0 这 2 位，对应 3 个点阵(第 7、8、9 个）会显示出来
	//这个液晶驱动 IC 的每个列地址管 3 个点阵，无法分开，所以第 7、8 个点阵会连累到第 9 个点阵，结果是每次显示 9 个点阵，只不过第 9 个点阵会补“0”
	//如果第 9 个点阵本来有显示内容，就会被无情地清掉
}


void LCD_ShowChar(uint16_t x, uint16_t y, uint8_t num)
{
    uint8_t temp, i;
    const uint8_t *font_ptr = NULL; // 先创建一个空的字库指针

    // 判断字符类型，并获取正确的字库地址
    if (num >= '0' && num <= '9')
    {
        // 如果是数字
        font_ptr = &asc2_1608_number[num - '0'][0];
    }
    else if (num >= 'A' && num <= 'Z')
    {
        // 如果是大写字母
        font_ptr = &asc2_1608_UppL[num - 'A'][0];
    }
    // else if (num >= 'a' && num <= 'z')
    // {
    //     // 如果以后您添加了小写字母字库，可以取消这里的注释
    //     font_ptr = &asc2_1608_LowL[num - 'a'][0];
    // }
	else if (num == ' ')
    {
        // 如果是空格，特殊处理，不查找字库
        // 直接准备绘制一个 8x16 的空白区域
        lcd_address(x, y, 8, 16);
        LCD_WriteRAM_Prepare();
        for (i = 0; i < 16; i++)
        {
            transfer_mono_data_8pixel(0x00); // 发送全0数据，即背景色
        }
        return; // 空格处理完毕，直接返回
    }
    else
    {
        // 对于其他所有字符（比如空格、标点符号等），我们暂时不显示
        // 您也可以在这里指向一个“未知字符”的字模，比如一个方框
        return; // 直接返回，不进行后续操作
    }

    // 如果找到了合法的字库
    if (font_ptr)
    {
        lcd_address(x, y, 8, 16); // 设置显示窗口为 8x16
        LCD_WriteRAM_Prepare();   // 准备写GRAM

        for (i = 0; i < 16; i++) // 循环16行，显示一个8x16的字符
        {
            temp = font_ptr[i]; // 取出当前行8个像素的数据
            transfer_mono_data_8pixel(temp); // 调用我们已经分析过的函数，将这8个像素的数据发送出去
        }
    }
}

